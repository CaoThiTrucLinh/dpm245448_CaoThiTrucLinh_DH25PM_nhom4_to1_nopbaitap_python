# Các kiểu dữ liệu cơ bản trong Python

# 1. Số nguyên (int)
so_nguyen = 10

# 2. Số thực (float)
so_thuc = 10.5

# 3. Số phức (complex)
so_phuc = 3 + 4j

# 4. Chuỗi (str)
chuoi = "Hello Python"

# 5. Boolean (bool)
dung_sai = True

# 6. Danh sách (list)
danh_sach = [1, 2, 3]

# 7. Bộ (tuple)
bo = (1, 2, 3)

# 8. Tập hợp (set)
tap_hop = {1, 2, 3}

# 9. Từ điển (dict)
tu_dien = {"ten": "Linh", "tuoi": 20}

print(type(so_nguyen), type(so_thuc), type(so_phuc))
print(type(chuoi), type(dung_sai))
print(type(danh_sach), type(bo), type(tap_hop), type(tu_dien))