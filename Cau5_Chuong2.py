# 3. Docstring (dùng để mô tả hàm/lớp/module)
def tinh_tong(a, b):
    """
    Hàm này dùng để tính tổng của hai số a và b.
    Đây là một docstring — dạng chú thích đặc biệt.
    """
    return a + b